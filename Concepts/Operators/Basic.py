# Basic Operations
num1 = 5
num2 = 10

# [=, -, *, /]
print("num1 + num2 =>", num1 + num2)
print("num1 - num2 =>", num1 - num2)
print("num1 * num2 =>", num1 * num2)
print("num1 / num2 =>", num1 / num2)

# Exponential Operator
print("2 ** 3 =>", 2 ** 3)

# Modulus Operator
print("10 % 3 =>", 10 % 3)

# Float Division [rounds result to the floor value]
print("3.8 // 2 =>", 3.86 // 2)
print("10 // 3 =>", 10 // 3)
