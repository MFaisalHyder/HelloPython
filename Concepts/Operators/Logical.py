# Logical Operators

true = True
false = False

# [and, or, not of]
print("true and false", true and false)
print("true or false", true or false)
print("not true", not true)

# In Numeric, any thing equals to 0 is deemed False
print("2 and 3 =>", 2 and 3)  # 3
print("-277 and 3 =>", -277 and 3)  # 3
print("0 and 3 =>", 0 and 3)  # 0

print("2 or 3 =>", 2 or 3)  # 2,
print("-2 or 3 =>", -2 or 3)  # -2
print("-2 or -3 =>", -2 or -3)  # -2
print("0 or -3 =>", 0 or -3)  # -3

print("not 2 =>", not 2)
print("not -3 =>", not -3)
print("not 0 =>", not 0)
print("not false", not false)
