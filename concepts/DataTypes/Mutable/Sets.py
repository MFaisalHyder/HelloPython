"""
Set is an unordered collection of immutable items
egSet = {1,2,'Faisal'}
"""

egSet = {10, 5, 2, 'Hyder', 3, 2, 'Zinc', 'Faisal'}
print(egSet)

set1 = {1, 3, 5, 9, 0, 6}
set2 = {2, 3, 7, 8, 6}

# Operations
# Union [|, common to arithmetic's union operation]
print(set1 | set2)

# Intersection [&, common to arithmetic's union operation]
print(set1 & set2)

# Difference [-, common to arithmetic's union operation]
print(set1 - set2)
